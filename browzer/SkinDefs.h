#define MBCONFIG_READ_OTHER_COLORS(_MBREG_OBJ_,\
_MBCOLOROTHERFG_,_MBCOLOROTHERBG_,\
_MBCOLORBUTTONSFGOUT_,_MBCOLORBUTTONSBGOUT_,\
_MBCOLORBUTTONSFGHOVER_,_MBCOLORBUTTONSBGHOVER_,\
_MBCOLORBUTTONSFGIN_,_MBCOLORBUTTONSBGIN_,\
_MBOTHERBUTTONHOV_,_MBOTHERBUTTON3D_)\
	_MBCOLOROTHERFG_	= _MBREG_OBJ_.Read("OtherColorFg",RGB(255,255,255));\
	_MBCOLOROTHERBG_	= _MBREG_OBJ_.Read("OtherColorBg",0);\
	_MBCOLORBUTTONSFGOUT_	= _MBREG_OBJ_.Read("OtherColorBtnFgOut",0);\
	_MBCOLORBUTTONSBGOUT_	= _MBREG_OBJ_.Read("OtherColorBtnBgOut",RGB(150,150,150));\
	_MBCOLORBUTTONSFGHOVER_	= _MBREG_OBJ_.Read("OtherColorBtnFgHover",0);\
	_MBCOLORBUTTONSBGHOVER_	= _MBREG_OBJ_.Read("OtherColorBtnBgHover",RGB(150,150,150));\
	_MBCOLORBUTTONSFGIN_	= _MBREG_OBJ_.Read("OtherColorBtnFgIn",_MBCOLORBUTTONSFGHOVER_);\
	_MBCOLORBUTTONSBGIN_	= _MBREG_OBJ_.Read("OtherColorBtnBgIn",_MBCOLORBUTTONSBGHOVER_);\
	_MBOTHERBUTTONHOV_		= (TRUE && _MBREG_OBJ_.Read("OtherBtnHover",0));\
	_MBOTHERBUTTON3D_		= (TRUE && _MBREG_OBJ_.Read("OtherBtn3d",0));

#define MBCONFIG_READ_COLOR_3D(_MBREG_OBJ_,_MBLOCATION_,_MB_IN_UL_,_MB_IN_LR_,_MB_OUT_UL_,_MB_OUT_LR_)\
	_MB_IN_UL_  = _MBREG_OBJ_.Read("Color3d" + CS(_MBLOCATION_) + "InUL",0);\
	_MB_IN_LR_  = _MBREG_OBJ_.Read("Color3d" + CS(_MBLOCATION_) + "InLR",0);\
	_MB_OUT_UL_ = _MBREG_OBJ_.Read("Color3d" + CS(_MBLOCATION_) + "OutUL",0);\
	_MB_OUT_LR_ = _MBREG_OBJ_.Read("Color3d" + CS(_MBLOCATION_) + "OutLR",0);

#define MBCONFIG_WRITE_COLOR_3D(_MBREG_OBJ_,_MBLOCATION_,_MB_IN_UL_,_MB_IN_LR_,_MB_OUT_UL_,_MB_OUT_LR_)\
	_MBREG_OBJ_.Write("Color3d" + CS(_MBLOCATION_) + "InUL",MBUtil::CrToRGB(_MB_IN_UL_));\
	_MBREG_OBJ_.Write("Color3d" + CS(_MBLOCATION_) + "InLR",MBUtil::CrToRGB(_MB_IN_LR_));\
	_MBREG_OBJ_.Write("Color3d" + CS(_MBLOCATION_) + "OutUL",MBUtil::CrToRGB(_MB_OUT_UL_));\
	_MBREG_OBJ_.Write("Color3d" + CS(_MBLOCATION_) + "OutLR",MBUtil::CrToRGB(_MB_OUT_LR_));

#define MBCONFIG_READ_SKIN_DEFS(_MBCONFIG_OBJ_,_REGISTRY_KEY_OBJ_) \
	_REGISTRY_KEY_OBJ_.init(_MBCONFIG_OBJ_.getSkin(MB_SKIN_DEF));\
	_REGISTRY_KEY_OBJ_.ReadFile();\
	{\
		RegistryKey RegObjTmp(_MBCONFIG_OBJ_.getSkin(MB_SKIN_DEF_CUSTOM));\
		RegObjTmp.ReadFile();\
		_REGISTRY_KEY_OBJ_.Copy(RegObjTmp);\
	}

